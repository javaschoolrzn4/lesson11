package com.javarzn.calculator;

public class Calculator {
    public long add(long a, long b) {
        return a + b;
    }

    public long minus(long a, long b) {
        return a - b;
    }

    public long umn(long a, long b) {
        return a * b;
    }

    public long del(long a, long b) {
        return a % b;
    }


}
